package openj9.internal.criu;

import static java.lang.annotation.ElementType.CONSTRUCTOR;
import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * This annotation is used to indicate to the JVM if any thread enters a method with
 * this annotation, it is not safe to take a checkpoint. The JVM will then wait until
 * the thread exits that method before attempting to take a checkpoint. This annotation
 * is only respected when applied to classes loaded by the bootloader.
 *
 */
@Retention(RUNTIME)
@Target({ METHOD, CONSTRUCTOR })
public @interface NotCheckpointSafe {}
